def apply_replacements(text, entries, new_exs):
    edits = []
    for i, new_text in new_exs.items():
        m = entries[i]["ex_match"]
        edits.append((m.start(1), m.end(1), js_escape(new_text)))
    edits.sort(key=lambda x: x[0], reverse=True)
    for start, end, repl in edits:
        text = text[:start] + repl + text[end:]
    return text


def phase_apply(match_cache, translate_cache):
    total_fixed = 0
    total_fallback = 0
    for path, lang, sep in FILES:
        text, entries = scan_file(path)
        new_exs = {}
        file_fixed = file_fallback = 0
        for i, e in enumerate(entries):
            if not e["placeholder"]:
                continue
            sent = match_cache.get(e["word"])
            tr = ""
            if sent:
                piece = sanitize_piece(sent, sep)
                key = f"{lang}|tr|{piece}"
                tr = translate_cache.get(key, "")
            if sent and tr:
                new_exs[i] = f"{ensure_period(piece)}{sep}{ensure_period(tr)}"
                file_fixed += 1
            else:
                new_exs[i] = f"{NOTFOUND_TEXT[lang]}{sep}{NOTFOUND_TR}"
                file_fallback += 1
        if not new_exs:
            continue
        updated = apply_replacements(text, entries, new_exs)
        with open(path, "w", encoding="utf-8", newline="\n") as f:
            f.write(updated)
        total_fixed += file_fixed
        total_fallback += file_fallback
        print(f"[apply] {os.path.basename(path)}: fixed={file_fixed} fallback_used={file_fallback}", flush=True)
    print(f"[apply] DONE total_fixed_with_real_sentence={total_fixed} total_honest_fallback={total_fallback}", flush=True)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--phase", default="all", choices=["scan", "index", "translate", "apply", "all"])
    ap.add_argument("--limit", type=int, default=None, help="Only process first N unique words per language (smoke test)")
    args = ap.parse_args()

    if args.phase in ("scan", "all"):
        target_words = phase_scan(args.limit)
    if args.phase == "scan":
        return

    if args.phase in ("index", "all"):
        if args.phase == "index":
            target_words = phase_scan(args.limit)
        match_cache = phase_index(target_words, args.limit)
    if args.phase == "index":
        return

    if args.phase in ("translate", "all"):
        if args.phase == "translate":
            match_cache = load_json(WORD_MATCH_CACHE)
        translate_cache = phase_translate(match_cache)
    if args.phase == "translate":
        return

    if args.phase in ("apply", "all"):
        if args.phase == "apply":
            match_cache = load_json(WORD_MATCH_CACHE)
            translate_cache = load_json(TRANSLATE_CACHE)
        phase_apply(match_cache, translate_cache)


if __name__ == "__main__":
    main()
