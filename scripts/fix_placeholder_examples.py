#!/usr/bin/env python3
"""Fix low-quality, self-referential placeholder EXAMPLE sentences across
data/*.js (English, German -- French has none of this issue, verified).

Three known placeholder wordings (all mechanically generated by earlier
bulk-import/enrichment scripts in this project, never real usage examples):
    EN: 'I am learning the word "X". - "X" kelimesini öğreniyorum.'
    DE: 'Ich lerne das Wort/Verb "X". - "X" kelimesini öğreniyorum.'
    EN (synant): '"X" is a useful word to know.;"Y" bilinmesi yararlı bir kelimedir.'
These sentences are contentless (any word fits grammatically -- they teach
nothing about the word's actual usage), reported as "useless" by the user.

Strategy: source a REAL example sentence for each affected word from the
Tatoeba open sentence corpus (CC BY 2.0 FR, bulk per-language TSV export --
https://tatoeba.org/en/downloads), which is:
  - Free, no API key, no per-request rate limit (one-time bulk download,
    then pure local text matching -- avoids the dictionaryapi.dev/Wiktionary
    per-word rate-limit walls hit repeatedly in earlier sessions).
  - Real, natural, contributor-written sentences -- appropriate for a
    language-learning app (this is literally Tatoeba's purpose).
Falls back to a fuzzy (inflection-tolerant) match for words not found
verbatim (same common-prefix heuristic as shared.js's czFindSpan()), and to
an HONEST "no example sentence available" fallback (never a fabricated
sentence) for words with no corpus hit at all -- matches this project's
established fallback precedent for un-sourceable dictionary content.

Chosen sentences are machine-translated to Turkish (Google Translate free
endpoint, same proven threaded+rate-limited pattern as
translate_and_build_synant.py) to fill the established "native - Turkish"
(or "native;Turkish" for the *synant*.js files) format.

Phases (run in order; each is resumable via its own on-disk cache):
    scan       - find all placeholder entries in every target file, report counts.
    index      - download Tatoeba corpora, match each unique affected word to
                 a real sentence (or None), cache to WORD_MATCH_CACHE.
    translate  - translate every matched sentence to Turkish, cache to
                 TRANSLATE_CACHE.
    apply      - rewrite each data file, replacing every placeholder example.
    all        - run all four in sequence.

Usage: python scripts/fix_placeholder_examples.py [--phase all] [--lang both] [--limit N]
"""
