# Translation (Google Translate free endpoint -- proven pattern reused
# from translate_and_build_synant.py / fix_placeholder_definitions.py)
# ---------------------------------------------------------------------------

class RateLimiter:
    def __init__(self, min_interval):
        self.min_interval = min_interval
        self.lock = threading.Lock()
        self.next_time = 0.0

    def wait(self):
        with self.lock:
            now = time.time()
            start_at = max(now, self.next_time)
            self.next_time = start_at + self.min_interval
        delay = start_at - now
        if delay > 0:
            time.sleep(delay)


TRANSLATE_LIMITER = RateLimiter(0.15)  # ~6.6 req/s, matches earlier proven-safe rate


class Transient(Exception):
    pass


def translate_one(text, src, tgt, attempts=4, base_delay=0.6):
    q = urllib.parse.quote(text.replace("\n", " ").strip())
    url = (
        "https://translate.googleapis.com/translate_a/single"
        f"?client=gtx&sl={src}&tl={tgt}&dt=t&q={q}"
    )
    last_err = None
    for i in range(attempts):
        TRANSLATE_LIMITER.wait()
        try:
            req = urllib.request.Request(url, headers=HEADERS)
            with urllib.request.urlopen(req, timeout=20) as resp:
                data = json.loads(resp.read().decode("utf-8"))
            return "".join(seg[0] for seg in data[0])
        except Exception as e:
            last_err = e
        time.sleep(base_delay * (2**i))
    raise Transient(str(last_err))


def phase_translate(match_cache):
    translate_cache = load_json(TRANSLATE_CACHE)
    jobs = []
    seen = set()
    for path, lang, sep in FILES:
        _, entries = scan_file(path)
        for e in entries:
            if not e["placeholder"]:
                continue
            sent = match_cache.get(e["word"])
            if not sent:
                continue
            piece = sanitize_piece(sent, sep)
            key = f"{lang}|tr|{piece}"
            if key in translate_cache or key in seen or not piece:
                continue
            seen.add(key)
            jobs.append((lang, "tr", piece, key))
    print(f"[translate] distinct sentences to translate = {len(jobs)}", flush=True)
    if not jobs:
        return translate_cache
    done = skipped = 0
    start = time.time()
    with ThreadPoolExecutor(max_workers=8) as ex:
        futures = {ex.submit(translate_one, text, src, tgt): key for src, tgt, text, key in jobs}
        for fut in as_completed(futures):
            key = futures[fut]
            try:
                translate_cache[key] = fut.result()
            except Transient:
                skipped += 1
            done += 1
            if done % 200 == 0 or done == len(jobs):
                save_json(TRANSLATE_CACHE, translate_cache)
                elapsed = max(0.001, time.time() - start)
                rate = done / elapsed
                remain = (len(jobs) - done) / rate if rate else 0
                print(
                    f"[translate] {done}/{len(jobs)} ({rate:.1f}/s, ~{remain/60:.1f} min left, {skipped} skipped-for-retry)",
                    flush=True,
                )
    save_json(TRANSLATE_CACHE, translate_cache)
    return translate_cache


